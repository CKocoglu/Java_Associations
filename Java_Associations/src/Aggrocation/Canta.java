package Aggrocation;

public class Canta {
	private String color;
	
	public Canta() {
		this.color="red";
	}
	public Canta(String color) {
		this.color=color;
	}
	
	public String getColor() {
		return this.color;
	}
	public void setColor(String color) {
		this.color=color;
	}
}

